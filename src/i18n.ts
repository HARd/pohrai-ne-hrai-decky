export function t(lang: Language, key: keyof typeof i18n["uk"], vars?: Record<string, string | number>) {
  let str = i18n[lang]?.[key] || i18n["en"][key] || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, String(v));
    }
  }
  return str;
}
